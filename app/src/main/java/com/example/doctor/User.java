package com.example.doctor;

public class User {

    String firstName, lastName, number, email, cat;

    public String getFirstName() {
        return firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public String getNumber() {
        return number;
    }

    public String getEmail() {
        return email;
    }

    public String getCat() {
        return cat;
    }
}